package com.huangcf.mp3.vo;

public class Mp3VO {
	private String title;
	private String author;
	private String id;
	private String mp3_src;
	private String mp3_vocer_src;
	private String n;
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getMp3_src() {
		return mp3_src;
	}
	public void setMp3_src(String mp3_src) {
		this.mp3_src = mp3_src;
	}

	public String getMp3_vocer_src() {
		return mp3_vocer_src;
	}
	public void setMp3_vocer_src(String mp3_vocer_src) {
		this.mp3_vocer_src = mp3_vocer_src;
	}
	public String getN() {
		return n;
	}
	public void setN(String n) {
		this.n = n;
	}
}
